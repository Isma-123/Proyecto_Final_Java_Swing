    private void MostrarFacturar() { 
    // Recuperar los valores de los campos de texto
    String id_pelicula = jTextField2.getText().trim();
    String usuario = jTextField1.getText().trim();
    String pago = jTextField3.getText().trim();
    
    // Manejo de excepciones para la conversión de pago a entero
    int pagoStr;
    try {
        pagoStr = Integer.parseInt(pago);
    } catch (NumberFormatException e) {
        ;
        return;  // Salir del método si hay un error en la conversión
    }

    // Verificación de campos vacíos
    if (id_pelicula.isEmpty() || usuario.isEmpty() || pago.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debes llenar todos los campos.", "ERROR", JOptionPane.ERROR_MESSAGE);
        return;  // Salir del método si hay campos vacíos
    }
    
    // Formatear el mensaje de la factura
    String mensaje = "ID de la Película: " + id_pelicula + "\n" +
                     "Usuario: " + usuario + "\n" +
                     "Total: $" + pagoStr;
    
    // Mostrar la factura en un JOptionPane
    JOptionPane.showMessageDialog(this, mensaje, "FACTURA GENERADA", JOptionPane.INFORMATION_MESSAGE);
     jTextField2.setText(" ");
     jTextField1.setText("  ");
     jTextField3.setText("   ");
     
  
     
}


  
